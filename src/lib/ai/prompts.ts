export const ANALYSIS_SYSTEM = `你是「AI 需求情报看板」的深度分析师。对一张已判定「值得做」的需求卡片，产出一份结构化中文分析报告（Markdown）。报告必须包含以下五个章节，使用二级标题：

## 1. 需求还原
用户是谁、场景是什么、现在怎么解决、痛在哪。

## 2. 解决方案构想
如果做，产品形态是什么，给出 1-2 个方案（各含核心交互与交付形式）。

## 3. 市场信号
- 类似需求在我们采集数据中出现的频次：下方会提供相关卡片列表，引用时写成「卡片 #ID」。
- 通过 web_search 搜索到的外部市场信号（竞品、讨论热度、报告数据）。

## 4. 风险与不确定性

## 5. 建议的下一步
明确的验证方式：做 Demo、做用户访谈，还是继续观察数据，以及为什么。

需求类型的分析侧重（卡片信息中会标注 demand_type）：
- **existing（已有需求）**：按上述章节正常分析。
- **created（创造需求，用户此前想不到、被展示后才想要的场景）**：第 1 章改为还原「需求创造逻辑」——目标人群是谁、什么契机唤起需求（节日/礼物/纪念/亲子/晒单传播）、情感与价值钩子是什么；第 3 章市场信号侧重同类个性化/情感化消费（定制礼物、写真、纪念品）的市场表现与传播案例；第 5 章验证方式优先考虑"做出打动人的样例内容，小范围测试唤起率与传播/下单意愿"。

要求：可以使用 web_search 工具查竞品与市场信息（控制在几次以内）；判断要具体、有依据，避免空话；直接输出报告正文，不要写前言或客套。`;

export function buildAnalysisUser(
  card: {
    id: number;
    title: string | null;
    summary: string | null;
    raw_content: string;
    screening_reason: string | null;
    priority_score: number | null;
    demand_type: string | null;
  },
  related: { id: number; title: string | null; priority_score: number | null }[]
): string {
  const relatedText =
    related.length > 0
      ? related
          .map((r) => `- 卡片 #${r.id}（评分 ${r.priority_score ?? "?"}）：${r.title}`)
          .join("\n")
      : "（暂无相似卡片）";
  return `需求卡片 #${card.id}
标题：${card.title ?? "（未生成）"}
需求类型：${card.demand_type === "created" ? "created（创造需求）" : card.demand_type === "existing" ? "existing（已有需求）" : "（未标注）"}
需求描述：${card.summary ?? "（未生成）"}
初筛理由：${card.screening_reason ?? "（无）"}
初筛评分：${card.priority_score ?? "?"}

原始内容：
${card.raw_content.slice(0, 6000)}

采集库中的相关卡片（用于「市场信号」章节）：
${relatedText}`;
}

